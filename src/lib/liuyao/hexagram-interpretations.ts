/**
 * 获取卦象的分类解读
 */
export function getHexagramCategoryInterpretation(
  hexagramName: string,
  category: QuestionCategory,
  subType?: string
): CategoryInterpretation | null {
  const hexagram = HEXAGRAM_INTERPRETATIONS[hexagramName];
  if (!hexagram) return null;

  return hexagram.categories[category] || null;
}

/**
 * 获取卦象的整体解读
 */
export function getHexagramOverview(hexagramName: string): string | null {
  const hexagram = HEXAGRAM_INTERPRETATIONS[hexagramName];
  return hexagram?.overview || null;
}
